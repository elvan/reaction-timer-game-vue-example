<template>
  <div v-if="showBlock" @click="stopTimer" class="block">Click Me</div>
</template>

<script>
export default {
  props: ['delay'],

  data() {
    return {
      showBlock: false,
      timer: null,
      reactionTime: 0,
    };
  },

  mounted() {
    setTimeout(() => {
      this.showBlock = true;
      this.startTimer();
    }, this.delay);
  },

  updated() {
    console.log('Component updated');
  },

  unmounted() {
    console.log('Component unmounted');
  },

  methods: {
    startTimer() {
      this.timer = setInterval(() => {
        this.reactionTime += 10;
      }, 10);
    },

    stopTimer() {
      clearInterval(this.timer);
      this.timer = null;
    },
  },
};
</script>

